(ns shiftlefter.corpus.generator
  "Seeded corpus generator for runner-mechanics acceptance (sl-vs9m).

   (profile, seed) materializes .feature files + manifest.edn under
   target/corpus/<profile>-<seed>/ at test time. Corpus files are never
   committed: a committed corpus drifts from its generator; a generated one
   can't. Same seed + same generator-version = byte-identical output
   (test-enforced). Seed stability holds WITHIN a generator version only
   (seeds-per-version doctrine, D1) -- the verifier only ever compares a run
   against the manifest born in the same generation act.

   The manifest is the machine contract (D2): per-scenario name / feature /
   tags / expected-status / expected-fail-step / duration-floor-ms /
   serial-group. Scenario names encode outcomes for HUMANS ('S-012 fails at
   step 2'); nothing parses names.

   Profiles are DATA (D5) and step emission is table-driven, so future
   slices (e.g. the 0.6 macro era) are data changes, not code branches."
  (:require [babashka.fs :as fs]
            [clojure.spec.alpha :as s]
            [clojure.string :as str]))

(def generator-version
  "Bumped whenever generated bytes for a given (profile, seed) change."
  [1 1])

;; -----------------------------------------------------------------------------
;; Profiles (data, not code branches)
;; -----------------------------------------------------------------------------

(def profiles
  "Preset parameter maps. :default covers the tag matrix at ~8 features /
   ~40 scenarios; :parallel-stress is the same generator with bigger N and
   more serial groups for sl-q9wp wall-clock work (crank :sleep-range-ms
   there when q9wp needs longer scenarios -- a data change)."
  {:default
   {:allow-pending? true
    :sleep-range-ms [10 60]
    :filler-scenarios 12
    :filler-statuses [:passed :passed :passed :passed :failed :error :pending]
    :serial-groups 2
    :serial-scenarios-per-group 3
    :control-scenarios 3}

   :parallel-stress
   {:allow-pending? true
    :sleep-range-ms [10 30]
    :filler-scenarios 60
    :filler-statuses [:passed :passed :passed :passed :failed :error :pending]
    :serial-groups 4
    :serial-scenarios-per-group 4
    :control-scenarios 8}})

